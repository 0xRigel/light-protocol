pub fn test_rkyv() {
    // Create an instance of `MyDataVec`
    let value = MyDataVec {
        vec: vec![
            MyData {
                value: 42,
                label: vec![2u8; 512],
            },
            MyData {
                value: 100,
                label: vec![4u8; 512],
            },
        ],
    };

    // Serializing is as easy as a single function call
    let size = std::mem::size_of::<MyDataVec>();
    light_heap::bench_sbf_start!("rkyv_to_bytes");
    let bytes = rkyv::to_bytes::<_, 1232>(&value).unwrap();
    light_heap::bench_sbf_end!("rkyv_to_bytes");
    // Or you can customize your serialization for better performance
    // and compatibility with #![no_std] environments
    use rkyv::ser::{serializers::AllocSerializer, Serializer};

    light_heap::bench_sbf_start!("anchor_serialize");
    let mut anchor_bytes = Vec::<u8>::new();
    let bytes = AnchorSerialize::serialize(&value, &mut anchor_bytes).unwrap();
    light_heap::bench_sbf_end!("anchor_serialize");
    light_heap::bench_sbf_start!("anchor_deserialize");
    let bytes = <MyDataVec as AnchorDeserialize>::deserialize(&mut &anchor_bytes[..]).unwrap();
    light_heap::bench_sbf_end!("anchor_deserialize");

    light_heap::bench_sbf_start!("rkyv_serialize");
    let mut serializer = AllocSerializer::<0>::default();
    serializer.serialize_value(&value).unwrap();
    light_heap::bench_sbf_end!("rkyv_serialize");
    let bytes = serializer.into_serializer().into_inner();

    // You can use the safe API for fast zero-copy deserialization
    light_heap::bench_sbf_start!("deserialize_zerocopy");
    let archived = rkyv::check_archived_root::<MyDataVec>(&bytes[..]).unwrap();
    light_heap::bench_sbf_end!("deserialize_zerocopy");

    assert_eq!(archived, &value);

    // Or you can use the unsafe API for maximum performance
    light_heap::bench_sbf_start!("deserialize_zerocopy_unsafe");
    let archived = unsafe { rkyv::archived_root::<MyDataVec>(&bytes[..]) };
    light_heap::bench_sbf_end!("deserialize_zerocopy_unsafe");
    assert_eq!(archived, &value);

    // And you can always deserialize back to the original type
    light_heap::bench_sbf_start!("deserialize");
    let deserialized: MyDataVec = archived.deserialize(&mut rkyv::Infallible).unwrap();
    light_heap::bench_sbf_end!("deserialize");
    assert_eq!(deserialized, value);
}

